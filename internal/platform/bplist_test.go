func TestEncodeBplistStringRoundTrip(t *testing.T) {
	tests := []struct {
		name  string
		input string
	}{
		{"empty", ""},
		{"short ascii", "#cli #golang"},
		{"exactly 14 chars", "12345678901234"},
		{"exactly 15 chars", "123456789012345"},
		{"long ascii", "#automation #infrastructure #golang"},
		{"unicode cyrillic", "#настройка"},
		{"emoji", "#rocket 🚀"},
		{"mixed ascii unicode", "hello мир"},
		{"cjk", "日本語タグ"},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			encoded := EncodeBplistString(tt.input)

			// Must start with bplist00 header.
			if !bytes.HasPrefix(encoded, []byte(bplistHeader)) {
				t.Fatalf("missing bplist00 header")
			}

			decoded, err := DecodeBplistString(encoded)
			if err != nil {
				t.Fatalf("decode: %v", err)
			}
			if decoded != tt.input {
				t.Errorf("round-trip: got %q, want %q", decoded, tt.input)
			}
		})
	}
}

