func TestDecodeBplistStringErrors(t *testing.T) {
	tests := []struct {
		name string
		data []byte
	}{
		{"nil", nil},
		{"too short", []byte("bplist00")},
		{"wrong header", append([]byte("notplist"), make([]byte, 32)...)},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			_, err := DecodeBplistString(tt.data)
			if err == nil {
				t.Error("expected error, got nil")
			}
		})
	}
}
